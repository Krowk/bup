package beans;

public class Age {
	private double enfants;
	private double jeunes;
	private double adultes;
	private double senior;
	
	public double getEnfants() {
		return enfants;
	}
	public void setEnfants(double enfants) {
		this.enfants = enfants;
	}
	public double getJeunes() {
		return jeunes;
	}
	public void setJeunes(double jeunes) {
		this.jeunes = jeunes;
	}
	public double getAdultes() {
		return adultes;
	}
	public void setAdultes(double adultes) {
		this.adultes = adultes;
	}
	public double getSenior() {
		return senior;
	}
	public void setSenior(double senior) {
		this.senior = senior;
	}
	
}
